/// 주간 레포트 산출에 사용되는 상수 (매직 넘버 제거)
class ReportConstants {
  ReportConstants._();

  // ---------------------------------------------------------------------------
  // ACWR (Acute:Chronic Workload Ratio) 존
  // ---------------------------------------------------------------------------

  /// 언더트레이닝 상한선
  static const double acwrUndertraining = 0.8;

  /// 스위트 스팟 상한선 (이 초과 시 위험 존)
  static const double acwrSweetSpotMax = 1.3;

  /// 위험 존 상한선 (이 초과 시 심각한 오버트레이닝)
  static const double acwrDangerMax = 1.5;

  /// 만성 부하 산출에 사용할 주 수
  static const int chronicWindowWeeks = 4;

  // ---------------------------------------------------------------------------
  // RPE 임계값
  // ---------------------------------------------------------------------------

  /// 높은 평균 RPE 경고 기준
  static const double highRpeThreshold = 9.0;

  // ---------------------------------------------------------------------------
  // 실패율
  // ---------------------------------------------------------------------------

  /// 높은 세트 실패율 경고 기준 (30%)
  static const double highFailureRateThreshold = 0.30;

  // ---------------------------------------------------------------------------
  // 볼륨 성장
  // ---------------------------------------------------------------------------

  /// 의미 있는 볼륨 증가 기준 (%)
  static const double significantVolumeGrowthPercent = 5.0;

  // ---------------------------------------------------------------------------
  // 근육군 균형
  // ---------------------------------------------------------------------------

  /// 근육군 볼륨 비율이 최대 그룹의 이 비율 미만이면 불균형 경고
  static const double muscleImbalanceThreshold = 0.5;

  /// 불균형 검사 시 무시할 최소 볼륨 (kg) — 데이터 잡음 방지
  static const double muscleVolumeNoiseFloor = 500.0;

  // ---------------------------------------------------------------------------
  // 1RM 추정 (Epley formula)
  // ---------------------------------------------------------------------------

  /// Epley 공식 분모 상수: 1RM = weight × (1 + reps / epleyDivisor)
  static const double epleyDivisor = 30.0;

  // ---------------------------------------------------------------------------
  // 액션 아이템 기본 볼륨 감소율
  // ---------------------------------------------------------------------------

  /// ACWR 위험 존 진입 시 권장 볼륨 감소 비율 (%)
  static const double recommendedVolumeReductionPercent = 10.0;

  /// 근육군 불균형 시 권장 볼륨 증가 비율 (%)
  static const double recommendedMuscleBoostPercent = 20.0;

  /// 액션 아이템에서 권장하는 목표 RPE
  static const double recommendedTargetRpe = 8.0;
}
